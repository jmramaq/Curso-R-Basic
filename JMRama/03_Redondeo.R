## REDONDEO Y DECIMALES

x= 1.2568947

## MOSTRAR SOLO n CIFRAS INCLUYENDO LA PRIMERA
# print(x,n)
print(x,3)
print(2^50)
print(2^50,12)

## REDONDEO A n CIFRAS
# round(x,n)
round(x,2)

## PARTE ENTERA POR EXCESO (redondeo a la baja) DE X
# floor(x)
floor(x)

## PARTE ENTERA POR DEFECTO (redonde al alza) DE X
# ceiling(x)
ceiling(x)

## PARTE ENTERA DE X, SIN DECIMALES
# trunc(x)
trunc(x)